#Integers:

x = int(7)   # x will be 1
y = int(8.2) # y will be 2
z = int("34") # z will be 3

print(x)
print(y)
print(z)

#Floats:

x = float(8)     # x will be 1.0
y = float(8.8)   # y will be 2.8
z = float("09")   # z will be 3.0
w = float("0.7") # w will be 4.2

print(x)
print(y)
print(z)
print(w)

#Strings:

x = str("G20") # x will be 's1'
y = str(5)    # y will be '2'
z = str(9.0)  # z will be '3.0'


print(x)
print(y)
print(z)
