# many values and multiple var

x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# one value and multiple var

x = y = z = "Orange"
print(x)
print(y)
print(z)

# unpack a collection
# If you have a collection of values in a list, tuple etc

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)